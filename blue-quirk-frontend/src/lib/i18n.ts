const messages: Record<string, Dict> = { fr, en, ar };

/**
 * Translate a key for the given language, interpolating {placeholders}.
 * Falls back to English, then the raw key, if a string is missing.
 */
export function t(
  lang: string,
  key: string,
  vars?: Record<string, string | number>
): string {
  const dict = messages[lang] ?? messages.fr;
  let s = dict[key] ?? messages.en[key] ?? messages.fr[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}

/** Curried translator bound to a language: `const tt = makeT(lang)`. */
export function makeT(lang: string) {
  return (key: string, vars?: Record<string, string | number>) => t(lang, key, vars);
}

