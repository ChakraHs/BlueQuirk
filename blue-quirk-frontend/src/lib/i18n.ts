// src/lib/i18n.ts
// Lightweight storefront localization. The active language comes from the URL
// segment (params.lang) and is passed down to components. `t(lang, key)` looks
// up a string in the dictionary below, interpolating {placeholders}. Product and
// category CONTENT is localized separately by the backend translation system;
// this covers the UI chrome (labels, buttons, headings).
import type { LangCode } from "@/lib/lang";

export function withLang(lang: string, path: string) {
  if (!path.startsWith("/")) {
    path = `/${path}`;
  }
  return `/${lang}${path}`;
}

export function getDefaultLang() {
  return "fr";
}

